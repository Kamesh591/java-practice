package operators;

public class AssignmentOperators {
    public static void main(String[] args) {

        /*
        Below are the allowed operations for assignment operators
        =
        +=
        -=
        *=
        /=
        %=
         */
        int x=20;
        char ch=91;
        String name="kamesh";

         x+=20; //x=x+20
        System.out.println(x);
        System.out.println(ch);
        System.out.println(name);
    }
}
